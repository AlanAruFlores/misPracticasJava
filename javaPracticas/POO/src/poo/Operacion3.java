public class Operacion3 {
    
    public int sumar(int numero, int numero2)
    {
        int suma = numero + numero2;
        return suma;
    }
    public int restar(int numero, int numero2)
    {
        int resta = numero - numero2;
        return resta;
    }
    public int multiplicar(int numero, int numero2)
    {
        int multiplicacion = numero * numero2;
        return multiplicacion;
    }
    public int dividir(int numero, int numero2)
    {
        int division = numero / numero2;
        return division;
    }
}
